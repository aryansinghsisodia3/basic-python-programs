# This Program Calculates The Percentage Of Marks You Get Out Of Eighty

#English marks
eng=float(input("Enter your English marks: "))

#Science marks
sci=float(input("Enter your Science marks: "))

#Maths marks
mat=float(input("Enter your Maths marks: "))

#Social Science marks
sst=float(input("Enter your Social Science marks: "))

#French marks
fre=float(input("Enter your French marks: "))

#This will Print Percentage 
print ("Total", "Percentage", "Is:",(eng + sci + mat + sst + fre)/4)